export const genrateRandomParagraphs = (n = 50) => {
  return words
    .map((x) => ({ x, r: Math.random() }))
    .sort((a, b) => a.r - b.r)
    .map((a) => a.x)
    .slice(0, n)
    .join(" ");
};

export function pressKey(e) {
  let key = getKey(e);
  key.setAttribute("data-pressed", "on");
  setTimeout(function () {
    key.removeAttribute("data-pressed");
  }, 200);
}
export function getKey(e) {
  let location = e.location;
  let selector;
  if (location === KeyboardEvent.DOM_KEY_LOCATION_RIGHT) {
    selector = ['[data-key="' + e.keyCode + '-R"]'];
  } else {
    var code = e.keyCode || e.which;
    selector = [
      '[data-key="' + code + '"]',
      '[data-char*="' + encodeURIComponent(String.fromCharCode(code)) + '"]',
    ].join(",");
  }
  return document.querySelector(selector);
}

export const convertTime = (time) => {
  if (time <= 1) {
    return `${time} second`;
  }
  if (time < 60) {
    return `${time} seconds`;
  }
  if (time < 3600) {
    return `${Math.floor(time / 60)} minutes ${time % 60} seconds`;
  }
  return `${Math.floor(time / 3600)} hours ${Math.floor(
    (time % 3600) / 60
  )} minutes ${time % 60} seconds`;
};
